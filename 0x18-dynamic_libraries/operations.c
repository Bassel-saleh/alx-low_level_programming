/**
 * add - adds two numbers
 * @a: first integer
 * @b: second integer
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two numbers
 * @a: first integer
 * @b: second integer
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers
 * @a: first integer
 * @b: second integer
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: first integer
 * @b: second integer
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - calculates remainder
 * @a: first integer
 * @b: second integer
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
